package Exam;

public class Rectangle {

    private class Rectangle {


            private double width;
            private double height;

            public Rectrangle(double width, double height) {
                this.width = width;
                this.height = height;
            }

            public double getPerimetr() {
                return 2 * (width + height);
            }

            public double getArea() {
                return width * height;
            }

        }
}
